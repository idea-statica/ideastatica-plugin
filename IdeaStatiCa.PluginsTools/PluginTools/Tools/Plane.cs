namespace CI.Geometry3D
{
	/// <summary>
	/// Defines basic planes
	/// </summary>
	public enum Plane : int
	{
		/// <summary>
		/// XY Plane - Plane normal to Z axis
		/// </summary>
		XY,

		/// <summary>
		/// YZ Plane - Plane normal to X axis
		/// </summary>
		YZ,

		/// <summary>
		/// ZX Plane - Plane normal to Y axis
		/// </summary>
		ZX
	}
}
